#!/usr/bin/env node
// Scans public/portfolio/projects/ and writes lib/portfolioManifest.ts.
// Run via: node scripts/generate-portfolio-manifest.js
// Also runs automatically as part of `npm run build` via the prebuild hook.

const fs = require('fs')
const path = require('path')

const projectsDir = path.join(process.cwd(), 'public', 'portfolio', 'projects')
const outputPath = path.join(process.cwd(), 'lib', 'portfolioManifest.ts')

const IMAGE_EXTS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif'])
const filenameSorter = new Intl.Collator(undefined, { numeric: true, sensitivity: 'base' })

const toPublicPath = (...segments) =>
  `/portfolio/projects/${segments.map((segment) => encodeURIComponent(segment)).join('/')}`

const folders = fs
  .readdirSync(projectsDir)
  .filter((f) => fs.statSync(path.join(projectsDir, f)).isDirectory())
  .sort()

const manifest = folders.map((folder) => {
  const folderPath = path.join(projectsDir, folder)
  const files = fs
    .readdirSync(folderPath, { withFileTypes: true })
    .filter((entry) => entry.isFile() && IMAGE_EXTS.has(path.extname(entry.name).toLowerCase()))
    .map((entry) => entry.name)
    .sort((a, b) => filenameSorter.compare(a, b))
    .map((filename) => toPublicPath(folder, filename))

  if (files.length === 0) {
    console.warn(`⚠️  No images found in portfolio project folder: "${folder}"`)
  }

  return { title: folder, images: files }
})

const content = `// AUTO-GENERATED by scripts/generate-portfolio-manifest.js
// Do not edit manually. Re-run \`node scripts/generate-portfolio-manifest.js\` to update.

export type ProjectImageManifest = {
  title: string
  images: string[]
}

const portfolioManifest: ProjectImageManifest[] = ${JSON.stringify(manifest, null, 2)}

export default portfolioManifest
`

fs.mkdirSync(path.dirname(outputPath), { recursive: true })
fs.writeFileSync(outputPath, content, 'utf8')
console.log('✅ Portfolio manifest written to', path.relative(process.cwd(), outputPath))
